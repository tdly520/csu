DECIHEX	SEGMENT
	ASSUME	CS:DECIHEX
MAIN	PROC	FAR
REPEAT1:
		CALL	DECIBIN
		CALL	CRLF
		CALL	BINIHEX
		CALL	CRLF
		JMP	REPEAT1

MAIN	ENDP

DECIBIN	PROC	NEAR
		MOV	BX,0
NEWCHAR:	
		MOV	AH,1
		INT	21H
		SUB	AL,30H
		JL	EXIT
		CMP AL,9D
		JG	EXIT
		CBW
		XCHG	AX,BX
		MOV	CX,10D
		MUL	CX
		XCHG	AX,BX
		ADD	BX,AX
		JMP	NEWCHAR
		
EXIT:	RET
DECIBIN 	ENDP

BINIHEX	PROC	NEAR
		MOV	CH,4
ROTATE:	MOV	CL,4
		ROL	BX,CL
		MOV	AL,BL
		AND	AL,0FH
		ADD	AL,30H
		CMP	AL,3AH
		JL	PRINTIT
		ADD	AL,7H
PRINTIT:
		MOV	DL,AL
		MOV	AH,2
		INT	21H
		DEC	CH
		JNZ	ROTATE
		RET
		
BINIHEX	ENDP

CRLF	PROC	NEAR
		MOV		DL,0DH
		MOV		AH,2
		INT		21H
		MOV		DL,0AH
		INT		21H
		RET
CRLF	ENDP
DECIHEX	ENDS

		END	MAIN
			
